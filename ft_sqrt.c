#include "libft.h"

int	ft_sqrt(int nb)
{
	int c;

	c = 0;
	if (nb < 0)
		return (0);
	while (c * c <= nb && c < 46341)
	{
		if (c * c == nb)
			return (c);
		c++;
	}
	return (0);
}
